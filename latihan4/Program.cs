using System;

namespace ney
{
    class neyberjalan
    {
        static void Main(String[] args)
        {
            string Varstring = " saya adalah string";   // string data type 
            int Varint = 1;                             // integer data type
            double Vardouble = 9.12345678912345678d;    // double data type
            float Varfloat = 9.12345678912345678f; // pecahan data type
            bool Varbolean = true;         // Boolean data type
            char Varcharacter = 'A';       // Character data type
            Console.WriteLine("string : " + Varstring);     // Menampilkan variabel string
            Console.WriteLine("int : " + Varint);           // Menampilkan variabel integer
            Console.WriteLine("double : " + Vardouble);     // Menampilkan variabel double
            Console.WriteLine("float : " + Varfloat);       // Menampilkan variabel float
            Console.WriteLine("boolean : " + Varbolean);      // Menampilkan variabel boolean
            Console.WriteLine("Character : " + Varcharacter);   // Menampilkan variabel character

            // Disini saya mempelajari membuat variabel
            // Berserta menampilkan variabel yang dibuat.
        }
    }
}